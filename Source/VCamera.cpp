#include "VCamera.h"

//-----------------------------------------------------------------------------
//-----------------------------------------------------------------------------
VCamera::VCamera()
{
	Camera = 0;
	
	OpenCamera();
	
	cv::namedWindow("CurrentImage",cv::WINDOW_AUTOSIZE);
}

//-----------------------------------------------------------------------------
//-----------------------------------------------------------------------------
VCamera::~VCamera()
{	
}

//-----------------------------------------------------------------------------
//-----------------------------------------------------------------------------
void VCamera::OpenCamera(void)
{
	if(!Camera.isOpened())
	{
		std::cout << "Camera failed to open" << std::endl;
	}
}

//-----------------------------------------------------------------------------
//-----------------------------------------------------------------------------
void VCamera::RecordVideo(void)
{
	while(1)
	{
		Camera >> mCurrentFrame;
		
		cv::imshow(
			"CurrentImage",
			mCurrentFrame);
	}
}