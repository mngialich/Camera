#include "VgCamera.h"

//-----------------------------------------------------------------------------
//-----------------------------------------------------------------------------
VgCamera::VgCamera()
{
	Camera = 0;
	
	OpenCamera();
}

//-----------------------------------------------------------------------------
//-----------------------------------------------------------------------------
VgCamera::~VgCamera()
{	
}

//-----------------------------------------------------------------------------
//-----------------------------------------------------------------------------
void VgCamera::OpenCamera(void)
{
	if(!Camera.isOpened())
	{
		std::cout << "Camera failed to open" << std::endl;
	}
	else
	{
		std::cout << "Camera was opened" << std::endl;
	}
}

//-----------------------------------------------------------------------------
//-----------------------------------------------------------------------------
cv::Mat* VgCamera::TakeInitialImage(void)
{
	Camera >> mCurrentFrame;
	
	return(&mCurrentFrame);
}

//-----------------------------------------------------------------------------
//-----------------------------------------------------------------------------
void VgCamera::TakeImage(cv::Mat *Image)
{
	Camera >> *Image;
}

//-----------------------------------------------------------------------------
//-----------------------------------------------------------------------------
cv::VideoCapture VgCamera::GetVideoHandle(void)
{
	return(Camera);
}
